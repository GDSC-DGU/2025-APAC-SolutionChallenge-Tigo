import 'dart:async';

abstract class SystemProvider {
  /* ------------------------------------------------------------ */
  /* Initialize ------------------------------------------------- */
  /* ------------------------------------------------------------ */
  Future<void> onInit();

  /* ------------------------------------------------------------ */
  /* Default ---------------------------------------------------- */
  /* ------------------------------------------------------------ */
  bool get isLogin;

  /* ------------------------------------------------------------ */
  /* Getter ----------------------------------------------------- */
  /* ------------------------------------------------------------ */
  bool getFirstRun();

  /* ------------------------------------------------------------ */
  /* Setter ----------------------------------------------------- */
  /* ------------------------------------------------------------ */
  Future<void> setFirstRun(bool isFirstRun);

  Future<void> setLogin(bool value);
}

extension SystemProviderExt on SystemProvider {
  // System Attributes
  static const String isFirstRun = "isFirstRun";
}
